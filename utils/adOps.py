#advanced operators like square roots
def power(x,y):
    return x ** y
def squre_root(x):
    return math.sqrt(x)
def sine(x):
    return math.sin(math.radians(x))
def cosine(x):
    return math.cos(math.radians(x))
def tanget(x):
    return math.tan(math.radians(x))
def logarithm(x, base=10):
    return math.log(x, base)
